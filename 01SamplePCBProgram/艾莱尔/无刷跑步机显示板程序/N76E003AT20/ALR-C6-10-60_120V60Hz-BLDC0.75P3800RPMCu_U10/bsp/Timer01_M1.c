#include "N76E003.h"
#include "Common.h"
#include "Delay.h"
#include "SFR_Macro.h"
#include "Function_define.h"


void Timer0_Init(void)               //定时器0初始化
{
	TIMER0_MODE1_ENABLE;               //定时器0  模式1
	clr_T0M;                           //选择12分频
	TH0 = (65536-76)/256;              //60us初值高八位        (65536-1334) 960us
	TL0 = (65536-76)%256;              //60us初值第八位
  set_PT0H;                          //优先级
  clr_PT0;
  
	set_ET0;                           //开启定时器0中断
	set_TR0;                           //定时器0启动
}





